class Logic 
{
   void FindMax(int a, int b)
   {
    if(a > b)
    {
        System.out.println("Max number is  :"+a);
    }
    else if(b > a)
    {
        System.out.println("Max number is  :"+b);
    }
    
   }
   
} 

