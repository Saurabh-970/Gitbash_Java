class Logic 
{
   void SumofDigits(int iNo)
   {
        int iSum = 0;
        int iDigit =0;

        while(iNo != 0)
        {
         iDigit = iNo % 10;
         iSum = iSum + iDigit;
         iNo = iNo / 10;
         
        }
        System.out.println("Sum of digits are :"+iSum);
   }
}
