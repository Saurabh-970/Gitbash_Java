class Logic 
{
 void CalculatePower(int base, int exp)
 {
    int iCnt = 0;
    int iResult = 1;

    for(iCnt = 1 ; iCnt <= exp; iCnt++)
    {
      iResult = iResult *base;
    }
    System.out.println("Power is :" +iResult);
 }  
} 

