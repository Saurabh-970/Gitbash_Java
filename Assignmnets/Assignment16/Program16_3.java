class Logic 
{
   void FindFactorial(int iNo)
   {
        int iFact = 1; 
        int iCnt = 0;
        for(iCnt = 1; iCnt <=iNo ; iCnt++)
        {
            iFact = iFact * iCnt;
        }
        System.out.println("Factorial of 5 is :"+ iFact);
        
   }


}
 
