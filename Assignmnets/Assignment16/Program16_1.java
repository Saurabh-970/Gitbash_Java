class Logic 
{
   void calculatesum(int n)
   {
        int iNo = 0;
        int iCnt = 0;
        
        for(iCnt = 1; iCnt <= n; iCnt++)
        {
            iNo = iNo + iCnt;
        }
        System.out.println("Sum of natural number is :"+ iNo);
   }


}
 
