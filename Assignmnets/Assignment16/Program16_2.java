//
//  Entry point of main method
//
///////////////////////////////////////////////////////////////////////////////////////////

class Program16_2
{
   public static void main(String A [])
   {
    Logic lobj = new Logic();
    lobj.CheckEvenoOdd(7); 
   }
}

