class Logic 
{
   void CheckEvenoOdd(int iNo)
   {
        if(iNo % 2 ==0)
        {
         System.out.println(iNo +" Is Even number ");
        }
        else
        {
          System.out.println(iNo +" Is Odd number ");
        }
   }


}
 
