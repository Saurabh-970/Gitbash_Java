class Logic 
{
 void printoddnumber(int inum)
 {
   int iCnt = 0;

   for(iCnt = 1; iCnt<= inum; iCnt++)
   {
    if(iCnt % 2 != 0)
    {
      System.out.println(iCnt);
    }
   }
 }  
} 

